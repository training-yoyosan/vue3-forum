<template>
  <div v-if="asyncDataStatus_ready" class="col-large push-top">
    <h1>
      {{ thread.title }}
      <router-link :to="{ name: 'ThreadEdit', params: { id: this.id } }" class="btn-green btn-small" tag="button">
        Edit
      </router-link>
    </h1>

    <p>
      By <a href="#" class="link-unstyled">{{ thread.author?.name }}</a
      >,
      <AppDate :timestamp="thread.publishedAt" />
      .
      <span style="float: right; margin-top: 2px" class="hide-mobile text-faded text-small"
        >{{ thread.repliesCount }} replies by {{ thread.contributorsCount }} contributors</span
      >
    </p>

    <post-list :posts="threadPosts" />

    <post-editor @save="addPost" />
  </div>
</template>

<script>
import PostList from "@/components/PostList";
import PostEditor from "@/components/PostEditor";
import AppDate from "@/components/AppDate";
import { mapActions } from "vuex";
import asyncDataStatus from "@/mixins/asyncDataStatus";

export default {
  name: "ThreadShow",

  components: {
    PostList,
    PostEditor,
    AppDate,
  },

  props: {
    id: {
      required: true,
      type: String,
    },
  },

  mixins: [asyncDataStatus],

  computed: {
    threads() {
      return this.$store.state.threads.length > 0 ? this.$store.state.threads : [];
    },
    posts() {
      return this.$store.state.posts.length > 0 ? this.$store.state.posts : [];
    },
    thread() {
      return this.$store.getters.thread(this.id); // available also as this.$route.params.id
    },
    threadPosts() {
      return this.posts.filter((post) => post.threadId === this.id);
    },
  },

  methods: {
    ...mapActions(["fetchThread", "fetchPosts", "fetchUsers"]),
    addPost(eventData) {
      const post = {
        ...eventData.post,
        threadId: this.id,
      };
      this.$store.dispatch("createPost", post);
    },
  },

  async created() {
    const thread = await this.fetchThread({ id: this.id });

    const posts = await this.fetchPosts({
      ids: thread.posts,
    });
    await this.fetchUsers({
      ids: posts.map((post) => post.userId).concat(thread.userId),
    });
    this.asyncDataStatus_fetched();
  },
};
</script>

